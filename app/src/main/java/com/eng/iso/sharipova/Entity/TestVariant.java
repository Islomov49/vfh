public class TestVariant {
    String first;
    String second;
    String thirty;

    public TestVariant(String first, String second, String thirty) {
        this.first = first;
        this.second = second;
        this.thirty = thirty;
    }

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String getSecond() {
        return second;
    }

    public void setSecond(String second) {
        this.second = second;
    }

    public String getThirty() {
        return thirty;
    }

    public void setThirty(String thirty) {
        this.thirty = thirty;
    }
}
